-- ========== BEGIN TABLE CREATION SECTION ==========

CREATE TABLE League(
	Name VARCHAR(10) PRIMARY KEY NOT NULL,
	DH_allowed TINYINT
);

CREATE TABLE Division(
	Name VARCHAR(10)NOT NULL,
	League_Name VARCHAR(10) NOT NULL,
	PRIMARY KEY (League_Name, Name),
	FOREIGN KEY (League_Name) REFERENCES League(Name),
);

CREATE TABLE Team(
	Name VARCHAR(40) PRIMARY KEY NOT NULL,
	Wins INT NOT NULL DEFAULT 0,
	Losses INT NOT NULL DEFAULT 0,
	Mascot VARCHAR(40),
	Championships INT NOT NULL DEFAULT 0,
	City_or_State VARCHAR(40) NOT NULL,
	D_Name VARCHAR(10) NOT NULL,
	D_League VARCHAR(10) NOT NULL,
	FOREIGN KEY (D_League,D_Name) REFERENCES Division(League_Name, Name)
);

CREATE TABLE Stadium(
	Name VARCHAR(40) PRIMARY KEY NOT NULL,
	Capacity INT NOT NULL,
	Location VARCHAR(255) NOT NULL,
	Opened INT NOT NULL
);

CREATE TABLE Player(
	FName VARCHAR(40) NOT NULL,
	LName VARCHAR(40) NOT NULL,
	Date_of_Birth DATE NOT NULL,
	PRIMARY KEY (Fname, Lname, Date_of_Birth),
	Jersey_Num INT NOT NULL,
	Active_Roster TINYINT,
	Batting_Avg FLOAT(3) DEFAULT .000,
	MLB_Debut_Year INT,
	Bats VARCHAR(10),
	Throws VARCHAR(10),
	Starter TINYINT,
	T_Name VARCHAR(40),
	CHECK(Bats IN ('SWITCH', 'LEFT', 'RIGHT')),
	CHECK(Throws IN ('SWITCH', 'LEFT', 'RIGHT')),
	FOREIGN KEY (T_Name) REFERENCES Team(Name)
);

CREATE TABLE Position(
	Name VARCHAR(40) PRIMARY KEY,
	Number INT UNIQUE
);

CREATE TABLE Plays_in(
	S_Name VARCHAR(40) NOT NULL, 
	T_Name VARCHAR(40) NOT NULL,
	PRIMARY KEY (S_Name, T_Name),
	FOREIGN KEY (S_Name) REFERENCES Stadium(Name),
	FOREIGN KEY (T_Name) REFERENCES Team(Name)
);

CREATE TABLE Plays( 
	P_Name VARCHAR(40) NOT NULL, 
	P_FName VARCHAR(40) NOT NULL,
	P_LName VARCHAR(40) NOT NULL,
	P_Date_of_Birth DATE NOT NULL,
	PRIMARY KEY (P_Name, P_FName, P_LName,P_Date_of_Birth),
	FOREIGN KEY (P_Name) REFERENCES Position(Name),
	FOREIGN KEY (P_FName, P_LName, P_Date_of_Birth) REFERENCES Player(FName, LName, Date_of_Birth)
	);

